    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        try
        {
            String sql = "select station_name from stations";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            while(rs.next())
            {
                fromCombo.addItem(rs.getString("station_name"));
                toCombo.addItem(rs.getString("station_name"));
            }
            pst.close();
        }
        catch(SQLException se){}
    }//GEN-LAST:event_formWindowOpened

    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
        String from=(String) fromCombo.getSelectedItem(); 
        String to  =(String) toCombo.getSelectedItem();
        String cardno = cardnoTxt.getText();
        String temp = cardnoTxt.getText();
        String sql =  "select FARE,TIME from TRAINROUTES where FROMSRC=? AND TODES=?";
        String sql2=  "select CURRENTBALANCE from SMARTCARD where SMARTCARDNO=?";
        if(temp.equals(""))
        {
               JOptionPane.showMessageDialog(null,"Field is Empty!!");
               cardnoTxt.requestFocus();
               return;
        }
        try
        {
            pst = conn.prepareStatement(sql);
            pst2 = conn.prepareStatement(sql2);
            pst.setString(1,from);
            pst.setString(2,to);
            pst2.setString(1,cardno);
            
            rs = pst.executeQuery();
            if(rs.next())
            {
                String add1 = rs.getString("FARE");
                fareTxt.setText(add1);
                
                String add2 = rs.getString("TIME");
                timeTxt.setText(add2);
            }
            rs = pst2.executeQuery();
            if(rs.next())
            {
                String add = rs.getString("CURRENTBALANCE");
                balanceTxt.setText(add);
            }
        
        }
       catch(Exception e)
       {
           JOptionPane.showMessageDialog(null, e); 
       }
    }//GEN-LAST:event_searchButtonActionPerformed

    private void cardnoTxtFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_cardnoTxtFocusLost
        // TODO add your handling code here:
        String temp = cardnoTxt.getText();
        char ch;
       
        for(int offset=0;offset<temp.length();offset++)
        {
               ch=temp.charAt(offset);
               if(!(ch<='9'&&ch>='0'))
               {
                   JOptionPane.showMessageDialog(null,"incorrect values");
                   cardnoTxt.requestFocus();
                   break;
               }
        }    
    }//GEN-LAST:event_cardnoTxtFocusLost

