    private void bookButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookButtonActionPerformed
        // TODO add your handling code here:
        String sql = "insert into SMARTBOOK (FROMSRC,TODES,FARE,DATE,TIME,TRAVELTIME,SMARTCARDNO)"
                + "values (?,?,?,?,?,?,?) ";
        String from=(String) fromCombo.getSelectedItem(); 
        String to  =(String) toCombo.getSelectedItem();
        String temp = (String) fareTxt.getText();
        if(temp.equals(""))
        {
               JOptionPane.showMessageDialog(null,"Press the Search button First ");
               searchButton.requestFocus();
               return;
        }
        try
        {
             pst = conn.prepareStatement(sql);
             pst.setString(1,from);
             pst.setString(2,to);
             pst.setString(3,fareTxt.getText());
             pst.setString(4,dateTxt.getText());
             pst.setString(5,booktimeTxt.getText());
             pst.setString(6,timeTxt.getText());
             pst.setString(7,cardnoTxt.getText());
             pst.execute();
             dispose();
        }
        catch(SQLException e)
        {
            
        }
    }//GEN-LAST:event_bookButtonActionPerformed

